package ru.netology.domain;

public class UploadButton {
    private String photoButton;
    private String videoButton;
    private String audioButton;
    private String fileButton;

    public String getPhotoButton() {
        return photoButton;
    }

    public void setPhotoButton(String photoButton) {
        this.photoButton = photoButton;
    }

    public String getVideoButton() {
        return videoButton;
    }

    public void setVideoButton(String videoButton) {
        this.videoButton = videoButton;
    }

    public String getAudioButton() {
        return audioButton;
    }

    public void setAudioButton(String audioButton) {
        this.audioButton = audioButton;
    }

    public String getFileButton() {
        return fileButton;
    }

    public void setFileButton(String fileButton) {
        this.fileButton = fileButton;
    }
}
